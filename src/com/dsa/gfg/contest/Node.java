package com.dsa.gfg.contest;


public class Node {
		int data;
		Node left;
		Node right;

		public Node(int data) {
			super();
			this.data = data;
		}

		public int getData() {
			return data;
		}

		public void setData(int data) {
			this.data = data;
		}

		public Node getNext() {
			return left;
		}

		public void setNext(Node next) {
			this.left = left;
		}
		
		

}
